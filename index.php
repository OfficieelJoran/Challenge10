            <p1>Identiteitsdiefstal</p1> 

    <p2>

    Wat is Identiteitsdiefstal? <br>
    Identiteitsdiefstal is het stelen van persoonlijke gegevens via internet.
    Denk hierbij aan paspoortnummers, e-mailadressen, creditcardgegevens en tan- of pincodes voor internetbankieren.
    Met deze informatie kan de dief vervolgens online aankopen doen,
    of de informatie doorverkopen aan derden die het vervolgens gebruiken voor spam of andere criminele activiteiten. 
    Identiteitsdiefstal vindt vaak plaats door eerst een computer te infecteren met een virus of Trojaans paard om zo aan de gegevens te komen. <br>
    </p2>   </div>


