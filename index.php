    <p1>DDoS</p1> 

    <p2>

    Wat is een DDos aanval? <br>
    Distributed denial-of-service aanvallen (DDoS-aanvallen) zijn pogingen om een computer, computernetwerk of dienst onbruikbaar te maken voor de bedoelde gebruiker.
    Bij een distributed dos-aanval voeren meerdere computers tegelijk de aanval online uit.
    Hiervoor wordt vaak een botnet gebruikt, maar het kan ook gaan om meerdere personen die hun acties coördineren. <br>
    </p2> 

