    <p1>Virussen</p1> 

    <p2>

    Wat zijn virussen? <br>
    Virussen zijn kleine programma's die zich verstoppen in andere bestanden,
    bijvoorbeeld foto's of bestanden die u per mail ontvangt. 
    Eenmaal op uw computer kunnen ze flinke schade aanrichten door bijvoorbeeld bestanden te beschadigen of verwijderen. <br>
    </p2> 

    <p3> Malware </p3> 

    <p2>
    Wat is malware? <br>

    Malware is de overkoepelende term voor computersoftware of code met kwaadaardige bedoelingen.
    Malware probeert computers, computersystemen, netwerken en/of mobiele apparaten binnen te dringen,
    te beschadigen of uit te schakelen, vaak door (gedeeltelijk) de controle over te nemen over het apparaat. </p2>


