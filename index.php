    <p1>Ransomware</p1> 

    <p2>

    Wat is Ransomware? <br>
    Ransomware is een online chantagemethode door middel van malware.
    Ransomware is een programma dat een computer (of gegevens die erop staan) blokkeert en vervolgens van de gebruiker losgeld vraagt om de computer weer te 'bevrijden'. <br>
    </p2> 

    <p3> Phishing </p3> 

    <p2>
    Wat is Phishing? <br>

    Phishing is een verzamelnaam voor e-mail of internetpagina's die zich voordoen als vertrouwde instellingen en bedrijven,
    vaak uw bank of veilingsites, maar dat in werkelijkheid niet zijn. Ze zullen u vragen om wachtwoorden of
    een creditcardnummer te geven om die vervolgens te misbruiken en geld of gegevens van u te stelen. </p2>


