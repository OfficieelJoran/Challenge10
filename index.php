    <p1>Trojans</p1> 

    <p2>

    Wat zijn Trojans? <br>
    Een trojan is schadelijke software vermomd als een betrouwbaar programma.
    Als gebruiker denk je een betrouwbaar programma op je computer te installeren, 
    maar in werkelijkheid krijgt een crimineel daarmee toegang tot jouw computer. <br>
    </p2> 

    <p5> Onbeveiligd draadloos netwerk </p5> 

    <p2>
    Wat is Phishing? <br>

    Veel mensen maken op kantoor en thuis gebruik van een draadloos netwerk.
    Het is belangrijk dat u deze internetverbinding goed beveiligt zodat anderen geen
    gebruik kunnen maken van uw internettoegang of via deze verbinding kunnen inbreken in uw computer. </p2>


