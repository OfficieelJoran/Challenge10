    <p3> Botnet </p3>

    <p2> Wat is een Botnet? <br>
    Wat is een botnet?
    Een botnet is een groot aantal computers die allemaal door een virus zijn geïnfecteerd.
    Botnets worden door criminelen gebruikt om, zonder dat de gebruikers het weten, spam of virussen te versturen.
     Vaak hebt u als gebruiker niet door dat uw computer deel uitmaakt van een botnet. *VTM houdt zijn netwerk actief in de gaten om botnets te voorkomen. <br>
