    <p1> Spyware </p1> <br> <br>

    <p2>
    Wat is spyware? <br>
    Spyware is software die zich ongezien op de computer nestelt om internetgedrag in de gaten te houden.
    Deze informatie wordt gebruikt om gerichte spam te versturen of uw persoonlijke gegevens, bijvoorbeeld bankrekeningnummers,
    te verkopen of op andere wijze te misbruiken. Vaak krijgt u ook veel ongewenste reclame via pop-ups. </p2>  </div>


