    <p2>
    Niemand mag zomaar persoonsgegevens van een ander op internet publiceren.
    Dit mag in principe alleen als daarvoor een wettelijke grondslag is uit de Algemene verordenings gegevensbescherming (AVG)
    In de AVG staan *zes grondslagen*. Eén daarvan is toestemming.
    Vaak is om persoonsgegevens te publiceren op internet toestemming nodig van de mensen om wie het gaat. Maar niet altijd.
    Er geld bijvoorbeeld een uitzondering voor persoonlijk of huishoudelijk gebruik. In dat geval is de AVG niet van toepassing. </div> </div>


